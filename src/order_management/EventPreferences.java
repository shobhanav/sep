package order_management;

public class EventPreferences {
	
	private boolean decorations;
	private boolean food;
	private boolean drinks;
	private boolean parties;
	private boolean photos;
	public boolean isDecorations() {
		return decorations;
	}
	public void setDecorations(boolean decorations) {
		this.decorations = decorations;
	}
	public boolean isFood() {
		return food;
	}
	public void setFood(boolean food) {
		this.food = food;
	}
	public boolean isDrinks() {
		return drinks;
	}
	public void setDrinks(boolean drinks) {
		this.drinks = drinks;
	}
	public boolean isParties() {
		return parties;
	}
	public void setParties(boolean parties) {
		this.parties = parties;
	}
	public boolean isPhotos() {
		return photos;
	}
	public void setPhotos(boolean photos) {
		this.photos = photos;
	}
	
	

}
